#include <iostream>
#include <cmath>
using namespace std;
int main (){
    cout << "----------MATH----------" << endl;
    cout << "---Max and Min---" << endl;
    cout << "Max la: " << max(5, 10) << "\n";
    cout << "Min la: " << min(5, 10) << "\n";
    cout << endl;

    cout << "---C++ <cmath> Library---" << endl;
    //Nhớ thêm thư viện #cmath

    cout << "sqrt (64): " << sqrt(64) << "\n";
    cout << "round (2.6): " << round(2.6) << "\n";
    cout << "log (2): " << log(2) << "\n";
    cout << endl;


    return 0;
}